pub fn get_loading_text(frame: usize, spinner: &crate::tui::spinner::Spinner) -> String {
    let dots = match frame % 4 {
        0 => "",
        1 => ".",
        2 => "..",
        _ => "...",
    };
    format!("{}{}", spinner.current(), dots)
}
